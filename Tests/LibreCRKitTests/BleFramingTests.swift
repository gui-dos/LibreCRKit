    func testWriteFragmenter162Cert() throws {
        let frags = BleFraming.fragmentForWrite(phase1Phone162)
        XCTAssertEqual(frags.count, 9)
        // Each fragment: 2-byte LE offset + up to 18 bytes
        for (i, frag) in frags.enumerated() {
            let claimedOffset = Int(frag[0]) | (Int(frag[1]) << 8)
            XCTAssertEqual(claimedOffset, i * 18, "fragment \(i) offset")
            XCTAssertLessThanOrEqual(frag.count - 2, 18)
        }
        // First fragment carries the test-pattern header bytes.
        XCTAssertEqual(frags[0].subdata(in: 2..<20),
                       Data([0x03, 0x03, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06,
                             0x07, 0x08, 0x09, 0x0a, 0x0b, 0x0c, 0x0d, 0x0e, 0x0f, 0x10]))
        // Reassembling fragments gives the original message back.
        let roundTrip = try BleFraming.reassembleWrite(frags)
        XCTAssertEqual(roundTrip, phase1Phone162)
    }

    func testNotifyReassemblerSensorCert() throws {
        // Build wire-shape notifies: 1B seq (starting 0) + 19B chunk.
        let asm = BleFraming.NotifyReassembler()
        var seq: UInt8 = 0
        var idx = 0
        while idx < phase2Sensor140.count {
            let end = min(idx + 19, phase2Sensor140.count)
            var frag = Data([seq])
            frag.append(phase2Sensor140.subdata(in: idx..<end))
            try asm.feed(frag)
            seq &+= 1
            idx = end
        }
        XCTAssertEqual(asm.availableBytes, 140)
        let assembled = try asm.take(140)
        XCTAssertEqual(assembled, phase2Sensor140)
    }

    func testNotifyReassemblerSequenceGap() {
        let asm = BleFraming.NotifyReassembler()
        XCTAssertNoThrow(try asm.feed(Data([0x00, 0xaa])))
        XCTAssertThrowsError(try asm.feed(Data([0x02, 0xbb]))) { e in
            XCTAssertEqual(e as? BleFramingError, .sequenceGap(expected: 1, got: 2))
        }
    }

    func testTakeMoreThanAvailable() {
        let asm = BleFraming.NotifyReassembler()
        XCTAssertNoThrow(try asm.feed(Data([0x00, 0xaa, 0xbb])))
        XCTAssertThrowsError(try asm.take(5)) { e in
            XCTAssertEqual(e as? BleFramingError, .notEnoughBytes(have: 2, want: 5))
        }
    }
